package org.shadow.livecity.files;

public class Vector3 {

    private Float x;
    private Float y;
    private Float z;

    public Vector3(Float x, Float y, Float z) {
        this.x = x;
        this.y = y;
        this.z = z;
    }

    public Float getX() {
        return x;
    }

    public Float getY() {
        return y;
    }

    public Float getZ() {
        return z;
    }
}
